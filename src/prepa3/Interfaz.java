    private void ShowAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowAllButtonActionPerformed
        
        Personal[] aux = rp.getArrayPersonal();
        
        for (int i = 0; i < aux.length; i++) {
            
            if(aux[i] instanceof Profesor){
                this.jTextArea1.append("\n profesor:");
                this.jTextArea1.append("\n "+aux[i].toString());
            }
            else if(aux[i] instanceof Limpieza){
                this.jTextArea1.append("\n Limpieza:");
                this.jTextArea1.append("\n"+aux[i].toString());
            }
            else{
                this.jTextArea1.append("\n Seguridad:");
                this.jTextArea1.append("\n"+aux[i].toString());
            }
        }
        
        
    }//GEN-LAST:event_ShowAllButtonActionPerformed

