    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String rsp = this.jComboBox1.getSelectedItem().toString();
        Personal[] aux = rp.getArrayPersonal();
        
        for (int i = 0; i < aux.length; i++) {
            
            if(rsp == "Profesor"){
                
                if(aux[i] instanceof Profesor){
                    this.jTextArea1.append("/n "+aux[i].toString());
                }
            }
            else if(rsp == "Limpieza"){
                
                if(aux[i] instanceof Limpieza){
                    this.jTextArea1.append("\n "+aux[i].toString());
                }
            }
            else{
                if(aux[i] instanceof Seguridad){
                    this.jTextArea1.append("\n "+aux[i].toString());
                }
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

